// Variable for demonstration
let dayOfWeek = 3; // Assume 1 = Monday, 2 = Tuesday, ..., 7 = Sunday

// Switch statement
switch (dayOfWeek) {
    case 1:
        console.log("Today is Monday. Start of the work week!");
        break;
    case 2:
        console.log("Today is Tuesday. Keep going!");
        break;
    case 3:
        console.log("Today is Wednesday. Midweek already!");
        break;
    case 4:
        console.log("Today is Thursday. Almost there!");
        break;
    case 5:
        console.log("Today is Friday. The weekend is near!");
        break;
    case 6:
        console.log("Today is Saturday. Enjoy your weekend!");
        break;
    case 7:
        console.log("Today is Sunday. Rest and prepare for the week ahead.");
        break;
    default:
        console.log("Invalid day of the week.");
}

