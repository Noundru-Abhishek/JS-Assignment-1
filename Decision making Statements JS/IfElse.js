
// Variables for demonstration
let temperature = 25; // Temperature in degrees Celsius

// Conditional statements
if (temperature >= 30) {
    console.log("It's hot outside. Stay hydrated!");
} else if (temperature >= 20) {
    console.log("The weather is pleasant. Enjoy your day!");
} else if (temperature >= 10) {
    console.log("It's a bit chilly. Wear a jacket!");
} else {
    console.log("It's cold outside. Stay warm!");
}

