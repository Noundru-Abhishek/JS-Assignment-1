// Creating variables of different primitive types
let num = 42;                 // Number
let str = "Hello, World!";   // String
let bool = true;             // Boolean
let nul = null;              // Null
let undef = undefined;       // Undefined

// Printing values and their types to the console
console.log("Value of num:", num);                     // Output: 42
console.log("Type of num:", typeof num);               // Output: number

console.log("Value of str:", str);                     // Output: Hello, World!
console.log("Type of str:", typeof str);               // Output: string

console.log("Value of bool:", bool);                   // Output: true
console.log("Type of bool:", typeof bool);             // Output: boolean

console.log("Value of nul:", nul);                     // Output: null
console.log("Type of nul:", typeof nul);               // Output: object (historically, typeof null returns 'object')

console.log("Value of undef:", undef);                 // Output: undefined
console.log("Type of undef:", typeof undef);           // Output: undefined
